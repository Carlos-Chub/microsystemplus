function enviarCorreo($correoDesti, $nomUsu, $encabezado, $msj, $arch=[])
{
    //return ; 
    // echo json_encode([$msj.' --- '.$arch[0], '0']);
    // return;

    $msjForm = formDatos($arch, $msj);
    $mail = new PHPMailer(true);
    try {
        //Server settings configuracion del servidor 
        $mail->SMTPDebug = 0;   //0 desactivar 2 mantener activo  //Enable verbose debug output
        $mail->isSMTP();                                            //Send using SMTP
        // $mail->Host       = 'smtp.gmail.com';                     //Set the SMTP server to send through
        // $mail->SMTPAuth   = true;                                   //Enable SMTP authentication
        // $mail->Username   = 'sotecpro1@gmail.com';                     //SMTP username
        // $mail->Password   = 'esmavjorzwkrvnma';  //wwclhuddjqcaaxvw                             //SMTP password
        $mail->SMTPSecure = 'TLS'; //PHPMailer::ENCRYPTION_SMTPS;   'ssl'         //Enable implicit TLS encryption
        $mail->Port       = 587;                                    //TCP port to connect to; use 587 if you have set `SMTPSecure = PHPMailer::ENCRYPTION_STARTTLS`

        //Recipients a quien le envio el correo 
        $mail->setFrom('' . $correoDesti . '', 'Sotecpro-Microsystem Plus');
        $mail->addAddress('' . $correoDesti . '', '' . $nomUsu . '');     //Add a recipient

        //Content
        $mail->isHTML(true);                                  //Set email format to HTML
        $mail->Subject = '' . $encabezado . ''; //'Alerta IVE'
        $mail->Body    = '' . $msjForm . '';

        $mail->send();
        return 1; //Se envio  el correo 
    } catch (Exception $e) {
        //echo "Se jodio amigo : {$mail->ErrorInfo}";
        return 0; //No se envio el correo 
    }
}
